#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	if (fd != 0 && s)
	{
		while (*s)
		{
			write(fd, s, 1);
			s++;
		}
	}
	else
		return ;
	write(fd, "\n", 1);
}
